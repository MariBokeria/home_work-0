function oddCount(n) {
    // ვინაიდან ლუწი და კენტი რიცხვები მონაცვლეობით მოდიან,
    // n-ზე ნაკლები კენტი რიცხვების რაოდენობა იქნება უბრალოდ n-ის გაყოფა 2-ზე.
    return Math.floor(n / 2); // Math.floor() იყენებთ რომ მთელ რიცხვზე ჩამოვიყვანოთ
  }
  
  // მაგალითები:
  console.log(oddCount(7));  // 3
  console.log(oddCount(15)); // 7
  