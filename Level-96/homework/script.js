function fakeBin(x) {
    // ვაქციეთ სტრინგი მასივად (array) სიმბოლოების მიხედვით
    return x
      .split('') // გავყოთ თითოეულ სიმბოლოდ
      .map(digit => digit < '5' ? '0' : '1') // თუ რიცხვი ნაკლებია 5-ზე -> '0', სხვა შემთხვევაში -> '1'
      .join(''); // დავაბრუნოთ ისევ სტრინგად
  }
  
  // მაგალითები:
  console.log(fakeBin("45385593107843568")); // "01011110001100111"
  console.log(fakeBin("1234567890"));        // "0000111110"
  