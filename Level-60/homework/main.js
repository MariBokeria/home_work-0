function checkNumber(num) {
    if (num % 2 === 0 && num > 10) {
      console.log("რიცხვი არის ლუწი და 10-ზე მეტი");
    } else if (num % 2 !== 0 && num < 5) {
      console.log("რიცხვი არის კენტი და 5-ზე ნაკლები");
    } else if (num > 20 || num < 5) {
      console.log("რიცხვი არის 20-ზე დიდი ან 5-ზე ნაკლები");
    } else if (!(num > 10 || num < 5)) {
      console.log("რიცხვი არ არის არც 10-ზე მეტი და არც 5-ზე ნაკლები");
    } else {
      console.log("რიცხვი სხვა პირობას არ შეესაბამება");
    }
  }
  
  // ტესტი
  checkNumber(12); // რიცხვი არის ლუწი და 10-ზე მეტი
  checkNumber(3);  // რიცხვი არის კენტი და 5-ზე ნაკლები
  checkNumber(25); // რიცხვი არის 20-ზე დიდი ან 5-ზე ნაკლები
  checkNumber(7);  // რიცხვი სხვა პირობას არ შეესაბამება
  