function nthChar(words) {
    // ვამზადებთ შედეგს მასივის რუკირების (map) და გაერთიანების (join) საშუალებით
    return words.map((word, index) => {
      // თითოეული სიტყვიდან ვიღებთ შესაბამის ინდექსზე მდგომ ასოს
      return word[index];
    }).join(''); // ბოლოს ვაერთიანებთ ყველა ასოს სტრინგად
  }
  