# მომხმარებლისგან ვიღებთ წინადადებას
sentence = input("შეიყვანეთ წინადადება: ")

# გრძელ სიტყვებს დავუბრუნოთ მათი შეყვანის თანმიმდევრობით, მაგრამ თითოეული სიტყვა შევხვიოთ
words = sentence.split()  # წინადადება იყოფა სიტყვებად

# თითოეული სიტყვა შემოტრიალება და მათი შეყვანა ისევ იმ თანმიმდევრობით
reversed_words = [word[::-1] for word in words]

# შედეგის დაბეჭდვა
result = " ".join(reversed_words)
print("შედეგი:", result)
