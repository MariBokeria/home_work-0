function განაახლეშუქნიშანი(მდგომარეობა) {
    const ცვლილებები = {
        "მწვანე": "ყვითელი",
        "ყვითელი": "წითელი",
        "წითელი": "მწვანე"
    };
    return ცვლილებები[მდგომარეობა] || "არასწორი მდგომარეობა";
}

// გამოყენების მაგალითები:
console.log(განაახლეშუქნიშანი("მწვანე"));  // შედეგი: ყვითელი
console.log(განაახლეშუქნიშანი("ყვითელი")); // შედეგი: წითელი
console.log(განაახლეშუქნიშანი("წითელი"));  // შედეგი: მწვანე
