# მაქსიმალური მნიშვნელობის პოვნის ფუნქცია
def my_max(numbers):
    # თუ სია ცარიელია, ვუბრუნებთ None (ან შეგვიძლია შეცვალოთ კონკრეტული შეტყობინება)
    if len(numbers) == 0:
        return None

    # ვიწყებთ პირველი ელემენტით, როგორც მაქსიმუმი
    max_value = numbers[0]
    
    # ვივლით ყველა ელემენტზე სიაში
    for num in numbers:
        # თუ მიმდინარე ელემენტი უფრო დიდია, ვიდრე ნაპოვნი მაქსიმუმი
        if num > max_value:
            # განვაახლებთ მაქსიმუმს
            max_value = num

    return max_value

# მინიმალური მნიშვნელობის პოვნის ფუნქცია
def my_min(numbers):
    # თუ სია ცარიელია, ვუბრუნებთ None (ან შეგვიძლია შეცვალოთ კონკრეტული შეტყობინება)
    if len(numbers) == 0:
        return None

    # ვიწყებთ პირველი ელემენტით, როგორც მინიმუმი
    min_value = numbers[0]
    
    # ვივლით ყველა ელემენტზე სიაში
    for num in numbers:
        # თუ მიმდინარე ელემენტი უფრო მცირეა, ვიდრე ნაპოვნი მინიმუმი
        if num < min_value:
            # განვაახლებთ მინიმუმს
            min_value = num

    return min_value

# რიცხვების ჯამის პოვნის ფუნქცია
def my_sum(numbers):
    # დავასახელებთ ჯამს როგორც 0
    total = 0
    
    # ვივლით ყველა ელემენტზე სიაში
    for num in numbers:
        # დაამატებთ მიმდინარე ელემენტს ჯამში
        total += num

    return total

# ტესტირება ფუნქციების
numbers = [5, 1, 9, 3, 7]

print("მაქსიმალური მნიშვნელობა:", my_max(numbers))  # უნდა დაბეჭდოს 9
print("მინიმალური მნიშვნელობა:", my_min(numbers))  # უნდა დაბეჭდოს 1
print("რიცხვების ჯამი:", my_sum(numbers))  # უნდა დაბეჭდოს 25



